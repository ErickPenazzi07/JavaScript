//operadores de atribuição artimética

var x = 10;
var y = 5;

console.log("----------------------------");
console.log("x =", x, "| y =", y);

x += y; //x = y + x
console.log("x =", x, "| y =", y);

x -= y; //x = y - x
console.log("x =", x, "| y =", y);

x *= y; //x = y * x
console.log("x =", x, "| y =", y);

x /= y; //x = y / x
console.log("x =", x, "| y =", y);

x **= y; //x elevado  x
console.log("x =", x, "| y =", y);

x %= y; //x = y / x
console.log("x =", x, "| y =", y);

